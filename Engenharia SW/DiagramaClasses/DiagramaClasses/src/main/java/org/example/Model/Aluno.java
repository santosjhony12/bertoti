package org.example.Model;

public class Aluno {
    private String nome;
    private int ra;

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getNome() {
        return nome;
    }

    public void setRa(int ra) {
        this.ra = ra;
    }

    public int getRa() {
        return ra;
    }
}
